'''
切片,用于获取list或tuple的部分元素
'''
L = ['A','B','C','D','E']
print(L[0:3])   #获取前3个元素
print(L[:3])    #如果第一个索引为0,可以省略
print(L[1:3])   #从索引1开始取2个数据
print(L[-2:])   #负数索引情况,打印结果['D', 'E']
print(L[-2:0])  #角标交叉取值为null,打印结果[]

L2 = list(range(100))
print(L2[:10:2])    #每2个取一个数,取前10个
print(L2[:])    #复制list
